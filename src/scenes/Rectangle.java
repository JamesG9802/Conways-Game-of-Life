package scenes;
/*
  This class is a container for an object's coordinates and its width and height.
 */
public class Rectangle {
	int x, y, width, height;
	
	public Rectangle(int x, int y, int width, int height)
	{
		this.x = x;
		this.y = y;
		this.width = width;
		this.height = height;
	}
}
